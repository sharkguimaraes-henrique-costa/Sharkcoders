print("Calculadora de dois fatores")
op=input("Qual é a operação que queres fazer?")
pn=float(input("Qual é o primeiro número?"))
sn=float(input("Qual é o segundo número?"))
if op == "+":
    valor = pn + sn
elif op == "-":
    valor = pn - sn
elif op == "*":
    valor = pn * sn
elif op == "/":
    valor = pn / sn


